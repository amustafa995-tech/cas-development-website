/* ── Language System (EN / FR / AR) ── */
const LANG_CYCLE = ['en', 'fr', 'ar'];
const LANG_LABEL = { en: 'EN', fr: 'FR', ar: 'AR' };
const NEXT_LANG_LABEL = { en: 'FR', fr: 'AR', ar: 'EN' };

function initLanguage() {
  const saved = localStorage.getItem('cas-lang') || 'en';
  setLanguage(saved);
}

function toggleLanguage() {
  const current = localStorage.getItem('cas-lang') || 'en';
  const idx = LANG_CYCLE.indexOf(current);
  const next = LANG_CYCLE[(idx + 1) % LANG_CYCLE.length];
  setLanguage(next);
}

function setLanguage(lang) {
  if (!LANG_CYCLE.includes(lang)) lang = 'en';
  localStorage.setItem('cas-lang', lang);

  const html = document.documentElement;
  html.setAttribute('lang', lang);
  html.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

  // Restore originals first (cached on first translation in dataset.origText)
  document.querySelectorAll('[data-orig-text]').forEach(el => {
    const orig = el.dataset.origText;
    if (orig != null) el.textContent = orig;
  });
  document.querySelectorAll('[data-orig-html]').forEach(el => {
    const orig = el.dataset.origHtml;
    if (orig != null) el.innerHTML = orig;
  });

  // 1. Translate data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][lang]) {
      if (!el.dataset.origText) el.dataset.origText = el.textContent;
      el.textContent = translations[key][lang];
    }
  });

  // 2. Translate body text nodes (only for non-EN)
  if (lang !== 'en') translateTextNodes(lang);

  // 3. Update toggle button (shows the NEXT language)
  const langBtn = document.getElementById('langToggle');
  if (langBtn) {
    langBtn.textContent = NEXT_LANG_LABEL[lang];
    langBtn.setAttribute('aria-label', `Switch to ${NEXT_LANG_LABEL[lang]} language`);
  }
}

/* ── Scan and translate text nodes ── */
function translateTextNodes(lang) {
  let mappings;
  if (lang === 'fr') mappings = textMappings;
  else if (lang === 'ar') mappings = arabicMappings;
  else return; // EN = original text, no translation needed

  // Wide selector list covering ALL translatable elements across all pages
  const selectors = [
    'h1', 'h2', 'h3', 'h4',
    '.highlight',  // gradient-text spans inside h1/h2 hero titles
    '.overline',
    '.hero__content p', '.page-hero__content p',
    '.section-header p',
    '.entity-card p', '.entity-card h3',
    '.entity-card__subtitle', '.entity-card__link',
    '.service-card h3', '.service-card p',
    '.stat__label',
    '.cta-banner h2', '.cta-banner p', '.cta-banner .overline',
    '.footer__title', '.footer__brand p', '.footer__links a', '.footer__bottom p',
    '.glass-card strong', '.glass-card span',
    '.glass-card p',
    '.btn',
    '.news-article__date', '.news-article h3', '.news-article p',
    '.news-article__link', '.news-article__source',
    '.p-card__name', '.p-card__country',
    '.filter-bar__btn',
    '.mfg-intro__text h2', '.mfg-intro__text p', '.mfg-intro__text .overline',
    '.timeline__text',
    'section p', 'section h2', 'section h3',
    '[data-i18n]'
  ];

  const elements = document.querySelectorAll(selectors.join(', '));
  const processed = new Set();

  elements.forEach(el => {
    if (processed.has(el)) return;
    processed.add(el);

    if (el.closest('.nav__links') && el.hasAttribute('data-i18n')) return;
    if (el.id === 'langToggle') return;

    const fullText = el.textContent.trim();
    // Preserve only icon-like children (svg, .icon-inline, [data-lucide])
    const iconChildren = Array.from(el.children).filter(c =>
      c.tagName === 'SVG' || c.classList.contains('icon-inline') || c.hasAttribute('data-lucide')
    );

    if (fullText && mappings[fullText]) {
      if (!el.dataset.origHtml) {
        el.dataset.origHtml = el.innerHTML;
      }

      if (iconChildren.length > 0 && iconChildren.length === el.children.length) {
        // Only icon children present — replace text nodes, keep icons
        let replaced = false;
        el.childNodes.forEach(node => {
          if (node.nodeType === Node.TEXT_NODE && node.textContent.trim() && !replaced) {
            node.textContent = ' ' + mappings[fullText];
            replaced = true;
          } else if (node.nodeType === Node.TEXT_NODE) {
            node.textContent = '';
          }
        });
      } else {
        // Default: replace full content (original behavior for headings with <span class="highlight"> etc.)
        el.textContent = mappings[fullText];
      }
      return;
    }

    el.childNodes.forEach(node => {
      if (node.nodeType === Node.TEXT_NODE) {
        const nodeText = node.textContent.trim();
        if (nodeText && mappings[nodeText]) {
          node.textContent = node.textContent.replace(nodeText, mappings[nodeText]);
        }
      }
    });
  });
}

